function download() {
    var link= document.createElement('a');
    link.setAttribute('href', "RegistrApp.apk");

    if (isMobile === true){
        link.setAttribute('download', "apk_mobile");
    }else {
        link.setAttribute('download', "apk_desktop");
    }
    
    link.click();
}